    private void SaveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveButtonActionPerformed
        // TODO add your handling code here:
        Store_Password_Controller controller;
        try {
            if(jTextFieldApplicationName.getText().isBlank() || jTextFieldApplicationID.getText().isBlank() || jTextFieldPass.getText().isBlank() ){
                JOptionPane.showMessageDialog(rootPane, "Please fill in all the column");
            } else {
                controller = new Store_Password_Controller();
                controller.setUser(user);
                controller.handleEncRequest(jTextFieldApplicationName.getText(),jTextFieldApplicationID.getText(),jTextFieldPass.getText());
                trigger();
                dispose();
            }
        } catch ( InvalidKeySpecException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } catch (Exception ex) { 
            Logger.getLogger(AddPassword.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        } 
        
    }//GEN-LAST:event_SaveButtonActionPerformed

