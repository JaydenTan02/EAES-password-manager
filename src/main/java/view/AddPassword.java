    void trigger() {
        Point p = j.getLocation();
        MouseEvent event = new MouseEvent(j, MouseEvent.MOUSE_CLICKED, System.currentTimeMillis(),
                0, p.x, p.y, 1, false);
        j.dispatchEvent(event);
    }
